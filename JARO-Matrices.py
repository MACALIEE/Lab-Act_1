# Matrices

# a. 1x3 matrix
matrix_a = [4, 6, 2]
print("a. 1x3 matrix:")
print(matrix_a)
print()

# b. 2x2 matrix
matrix_b = [[3.5, 3.14], [7, 0.25]]
print("b. 2x2 matrix:")
for row in matrix_b:
    print(row)
print()

# c. 3x3 matrix
matrix_c = [[1, 0, 0], [0, 1, 0], [0, 0, 1]]
print("c. 3x3 matrix:")
for row in matrix_c:
    print(row)
print()
